namespace LSL.Enumerables.MarkdownTables;

/// <summary>
/// Default numeric formats
/// </summary>
public static class NumericFormats
{
    /// <summary>
    /// The default number format for <see cref="decimal"/>, <see cref="double"/> and <see cref="float"/> values
    /// </summary>
    public const string DefaultNumberFormat = "N2";

    /// <summary>
    /// The default  format for all integer types
    /// </summary>
    public const string DefaultIntegerFormat = "N0";
}